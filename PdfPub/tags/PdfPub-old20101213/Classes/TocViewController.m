#pragma mark -
#pragma mark Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
	//deselect row.
	[tableView deselectRowAtIndexPath:[tableView indexPathForSelectedRow] animated:YES];
	
	//close this view.
	PdfPubAppDelegate* appDelegate = (PdfPubAppDelegate*)[[UIApplication sharedApplication] delegate];
	PdfPubViewController* vc = appDelegate.viewController;
	[vc toggleNavigationBar];//hide navigation bar.
	switch (indexPath.row) {
		case 0:
			[vc renewPageWithIndex:0];
			break;
		case 1:
			[vc renewPageWithIndex:1];
			break;
		case 2:
			[vc renewPageWithIndex:5];
			break;
		default:
			break;
	}
	[vc dismissModalViewControllerAnimated:YES];
	//[appDelegate.viewController dismissModalViewControllerAnimated:YES];
}


